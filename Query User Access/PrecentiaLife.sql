	--	,'GLORY WATTIMENA'
	--	,'ANISA FIRDAUSI'
	--	,'ASHAR DENNI SETIAWAN'
	--	,'NURDIANA ISNAENI'
	
	--)
GROUP BY
	s.SUSERID ,
	s.SLOGINSTAT ,
	s.DUSERCREATEDT ,
	s.TMODDT ,
	s.TLASTPASSCHGDT ,
	s.SUSERID ,
	s.SNAME ,
	dept.SDESC,
	ghd.SGROUP,
	ghd.SDESC,
	module.LSEQID,
	module.SPROGID
ORDER BY
	'[' +s.SUSERID+ ']' + ' - ' + s.SNAME
	



