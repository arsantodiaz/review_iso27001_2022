SELECT DISTINCT
	'PrecentiaLife' AS 'App Name',
	'[' +s.SUSERID+ ']' + ' - ' + s.SNAME AS 'Username',
	s.SLOGINSTAT AS 'Status',
	s.DUSERCREATEDT AS 'Create Date',
	s.TMODDT AS 'Change Date',
	s.TLASTPASSCHGDT AS 'Last Change Password',
	s.SNAME AS 'Full Name',
	dept.SDESC As 'Dept Name',
	ghd.SGROUP AS 'Group Name',
	ghd.SDESC,
	module.LSEQID AS 'Menu Id',
	module.SPROGID AS 'Menu Name'
FROM 
	EQLIFE.PLS.SCMUSER AS s
	INNER JOIN
	(SELECT * FROM EQLIFE.PLS.PDTMASTERDET WHERE LFKMASTERHDR = 149)AS dept
	ON s.SDEPTCODE = dept.SCODE
	INNER JOIN
	EQLIFE.PLS.SCMUSERACCESS AS usg
	ON s.LSEQID = usg.LFKUSER
	INNER JOIN
	EQLIFE.PLS.SCMGROUPHDR AS ghd
	ON usg.LFKGROUPHDR = ghd.LSEQID
	INNER JOIN
	EQLIFE.PLS.SCMGROUPDET AS grpdet
	ON grpdet.LFKGROUPHDR = ghd.LSEQID
	LEFT JOIN
	EQLIFE.PLS.SCXUSERLOGHDR AS loghd
	ON loghd.LFKUSER = S.LSEQID
	LEFT JOIN
	EQLIFE.PLS.SCMMODULE AS module
	ON module.LSEQID = grpdet.LFKMODULE
--WHERE
